// hover时调整图片来源的js function

function hovergotop(element) {
    element.setAttribute('src', 'https://static.igem.wiki/teams/5371/gotop/gotop.gif');
}

function unhovergotop(element) {
    element.setAttribute('src', 'https://static.igem.wiki/teams/5371/gotop/gotop.svg');
}

function hoverbrand(element) {
    element.setAttribute('src', 'https://static.igem.wiki/teams/5371/header/brandgif.gif');
}

function unhoverbrand(element) {
    element.setAttribute('src', 'https://static.igem.wiki/teams/5371/header/teambrand.png');
}